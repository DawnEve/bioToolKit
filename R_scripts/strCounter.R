getUniqeArr=function(str){
  #获得数组
  arr=strsplit(str,"\\s+",per=T)
  arr=arr[[1]]
  arr
  length(arr)
  #去重复
  arr <- arr[!duplicated(arr)]
  arr
  length(arr)
  return(arr)
}

arr2string=function(arr){
  str="";
  len=length(arr)-1;
  for(i in 0:len){
    temp = paste(arr[i]," ")
    str =paste(str, temp, " ")
  }
  return(str)
}

